<script>
    $(document).ready(function() {
        var toggler = document.getElementsByClassName("caret");
        var i;
        for (i = 0; i < toggler.length; i++) {
            toggler[i].addEventListener("click", function() {
                this.parentElement.querySelector(".tree_nested").classList.toggle("tree_active");
                $(this).toggleClass("fa-plus-square fa-minus-square");
            });
        }
        openSelected();
        colorTree();

        $('#sidebar_tree .has-subs input').on("click", function(event) {
            checkUncheckAll(this.checked, $(this).parent().next("ul").attr("id"));
        });

        $('#sidebar_tree input').on("click", function(event) {
            var li = $(this).closest("ul").parent().find("input").attr("id");
            var ul = $(this).closest("ul").attr("id");
            colorTree();
        });

        function checkUncheckAll(checked, ul) {
            $("#" + ul + " > li").each(function(index) {
                if (checked) {
                    $(this).find("input").prop("checked", true);
                } else {
                    $(this).find("input").prop("checked", false);
                }
            });
        }

        function openSelected() {
            $("#sidebar_tree li").each(function(index) {
                if ($(this).find("input").is(':checked')) {
                    $(this).parent("ul").addClass("tree_active");
                }
            });
        }

        function colorTree() {
            var checked = [],
                startLevel = 2;
            //remove all item-back
            $("input").next("span").removeClass("sidebar-item-back");

            //get parent all checked
            $('input[type=checkbox]:checked').each(function(index) {
                if (!checked.includes($(this).attr("data-parent"))) {
                    checked.push($(this).attr("data-parent"));
                }
            });

            //itterate all checked parents
            for (i = 0; i < checked.length; i++) {
                var counter = 0,
                    items = $("#ul-" + checked[i] + " > li").length;

                $("#ul-" + checked[i] + " > li").each(function(index) {
                    if ($(this).find("input").is(':checked')) {
                        counter++;
                    }
                });

                if (counter > 0 && counter < items) {
                    $("#filter-" + checked[i]).addClass("partsub");
                    $("#filter-" + checked[i]).next("span").addClass("sidebar-item-back");
                    $("#filter-" + checked[i]).prop("checked", false);
                }
                if (counter === items) {
                    $("#filter-" + checked[i]).removeClass("partsub");
                    $("#filter-" + checked[i]).next("span").removeClass("sidebar-item-back");
                    $("#filter-" + checked[i]).prop("checked", true);
                }
            }

            for (i = startLevel; i >= 1; i--) {
                var partsub = $(".partsub").attr("data-parent");
            }
        }
    });
</script>
